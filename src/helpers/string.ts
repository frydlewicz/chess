export function genRandomString(length: number): string {
    const chars = [
        '1',
        '2',
        '3',
        '4',
        '5',
        '6',
        '7',
        '8',
        '9',
        'A',
        'B',
        'C',
        'D',
        'E',
        'F',
        'G',
        'H',
        'J',
        'K',
        'L',
        'M',
        'N',
        'P',
        'R',
        'S',
        'T',
        'U',
        'W',
        'X',
        'Y',
        'Z',
        'a',
        'b',
        'c',
        'd',
        'e',
        'f',
        'g',
        'h',
        'j',
        'k',
        'm',
        'n',
        'o',
        'p',
        'r',
        's',
        't',
        'u',
        'w',
        'x',
        'y',
        'z',
    ];

    return [...Array(length)].reduce((acu) => acu + chars[Math.floor(Math.random() * chars.length)], '');
}
